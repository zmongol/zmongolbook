ᢘᡭᡳᢚᡬᢑᡪᡱᡱᡪᡭᡧ ᢊᡪᢑᡪᡳᡪᢐ᠂ ᢚᡬᡪᢊᢊᡬᢓ ᡭᡧ ᡥᡭᡬᡫᡨ ᡬᡬᡧ ᡥᡭᡭᡱᡱᡪᢐ ᢌᡭᡪᢊᡱᡱᡭᢐ ᡥᡭᡬᢔᡭᡬᡨ ᡥᡭᢙᡳ ᢜᡭᡬᡱᡬᢥᡭᢙᡦ ᢙᡳ ᡥᡪᢑᡳᡪᡧ ᢜᡭᢔᡳ ᡳᡪᢝ ᡳᡪᢙᡭᢑᡪᡪᡪᢙᡪᢐᡨ ᢊᡪᢔᡪᡧ ᡥᡭᡬᢊᡪᡨ᠃ ᡥᡭᡪᢚᡪᢑᡬᢇ ᡯᡫ᠄

1ᡖ ᡥᡪᢚᡭᢇ ᢊᡪᢙᡪᢔᡳ ᡬᡫ ᢘᡭᢓ ᢘᡭᢓ ᢘᡭᡱᡫ ᢥᡭᢙᢑᡪᡳᡪᡨ᠃

2ᡖ MVS᠂ FVS ᡬᡪᢞᢊᡪᡨ ᡴᡭᢙᡭᢑᡬᢜᡪᡪᢙᡪᡫ ᡯᡭᡬᢋᡭᢊᡪᢙᡪᢔᡳ ᢘᡪᢎᢙᡪᡬᡨ ᡫ ᢊᡪᢞᡪᢊᢑᡪᢊᢔᡪᡧ ᡥᡭᡬᢊᡪᡫ᠃

3ᡖ ᢘᡭᡬᢞᢔᡳ ᡬᡬᡧ ᢥᡭᢙᡦ ᡳᡪᢝ ᢥᡭᢙᢑᡪᡳᡪᡨ᠂ ᡥᡪᡳᡬᢜᡪᡧ ᢥᡭᢙᡦ ᡳᡪᢝ ᢥᡭᢙᢑᡪᡪᡪᢔᡪᡧ ᡳᡬᢔᡫ᠃

4ᡖ ᡥᡭᡬᢔᡭᡬᡨ ᡭᡧ ᢚᡬᢊᢑᡪᢐ ᡫ ᡳᡪᢞᡪᡱᡱᡭᡧ ᡥᢚᡧ ᡬᡪᢋᡭᡪᢔᡫ᠂ ᢘᡭᢑᡭᡱᡱᡪᡫ ᡯᡫ ᢘᡪᢊᡪᢊᢔᡫ ᡸᡪᢞᡪᡧ ᢥᡭᢙᢑᡪᢛᡳ᠂

ᢚᡬᡪᢊᢊᡬᢓ ᡭᡧ ᡥᡭᡬᡫᡨ ᡬᡬᡧ ᡸᡪᡱᡱᡭᢚᡬᡧ ᡥᡭᡬᡬᡱᡱᡭᢞᢛᡬᡧ ᡳᡬᢚᡬᡬᡨ ᡭᡧ ᢔᡭᡬᢝ ᢔᡭᡬᢑᢙᡧ ᡬᡫ ᢔᡪᢞᢋᡭᡳᡪᡨ᠃

ᡳᡪᢞᡪᡱᡱᡭᡧ ᡬᡬᢞᡭᢇ ᡴᡭᢐ ᢜᡭᡬᡱᡬᢥᡭᢙᡦ ᢙᡳ ᢥᡭᢙᢑᡪᡪᡪᢔᡪᡧ ᢘᡭᡬᢞᢔᡳ ᡴᡭᢑᡭᡰᡨ᠃ ᡯᡬᢊᡪᡨ ᢥᡭᢙᡦ ᡯᡫ ᡸᡪᢎᡭᢇ ᡥᡬᢑᡪᢋᡭᡳ ᢙᡪᢊᡪᡧ ᢘᡭᡬᢞᡳᡪᡧ ᢘᡭᡬᢞᢔᡳ ᡸᡭᡳᡬᢞᡪᢛᡳ᠂ ᢘᡭᡬᢞᡳᡪᡨ ᡥᢚᡧ ᢘᡪᡳᡪᡪᡪᢔᡪᡧ ᡸᡭᡳᡬᢞᡪᢐ ᡫ ᢔᡬᡰᡨ ᢥᡭᢙᡦ ᡳᡪᢝ ᡥᡬᢑᡪᢞᡪᢋᡭᢑᢛᡳ᠂ FVS᠂ MVSᡬᡪᢞᢊᡪᡨ ᡯᡭᡬᢋᡭᢊᡪᢙᡪᢔᡳ ᢘᡪᢎᢙᡪᡬᡨ ᡥᢚᡧ ᡬᡪᡬᡬᢑᡪᢔᢊᡬᡳᡪᡨ᠃ ᡛ ᡸᡧ ᡜ ᡬᡫ ᡸᡭᢜᡪᢝ ᢥᡭᢙᢑᡪᡪᡪᢔᡪᡧ ᡫ ᡸᡪᢞᡪᡱᡱᡪᢞᡪᡫ᠃



ᡥᡪᢑᢙᡪᡫ ᡥᡬᢛᡪᡱᡱᡭᢝ ᡭᡧ ᢊᡪᢑᡧ ᡯᡫ ᢘᡭᡬᢞᢔᡳ ᡬᡬᡧ ᢥᡭᢙᡦ ᡳᡪᢝ ᢥᡭᢙᢑᡪᡪᡪᢔᡪᡧ ᡯᡫ ᡯᡬᢊᡪᢑᢊᡪᡧ ᡳᡬᢔᡫ᠃ ᢔᡭᢑᡭᡪᢊᡱᡱᡭᢓ ᢊᡪᢑᡧ ᡯᡫ ᡥᡪᢑᢙᡪᡧ ᡥᡬᢛᡪᡱᡱᡭᢝ ᡭᡧ ᢊᡪᢑᡧ ᡳᡪᡬᡬᡪᡪᢔᡪᡧ ᢚᡳ ᢘᡭᡬᢞᢔᡳ ᡬᡬᡧ ᢥᡭᢙᡦ ᢊᡪᢞᡪᢊᢑᡪᢊᢔᡪᡧ᠃ ᢘᡭᡬᢞᢣ ᡥᡭᢑᡭᢓ ᡭᡧ ᡥᡭᢑᡭᢓ ᡭᡧ ᡥᢚᡬᢊᡪᡨ ᡥᡪᢘᡧ ᢘᡭᡬᢞᢣ ᡭᡧ ᢔᡪᡬᡬᢛᡬᢞᡪᡱᡱᡭᢑᡭᡪᡪᢔᡪᡧ ᢔᡬᡰᡨ ᢘᡭᡬᢞᢣ ᡳᡬᢚᡬᡬᡨ ᢚᡳ ᢘᡭᡬᢞᢔᡳ ᡬᡬᡧ ᢥᡭᢙᡦ ᡬᡪᢞᡭᡪᡪᢔᡪᡧ ᢊᡪᢋᡭ ᢙᡳ ᡥᡬᢑᡪᢋᡭᢙᡪᢋᡭ ᡥᡭᡬᢊᡪᡫ᠂ ᡥᡪᢞᡳ ᡸᡪᢑᢇᡨ ᡬᡬᡧ ᢊᡪᢞᡪᢊᢑᡪᢊᡪᡭᡧ ᡳᡪᡬᡬᢉᡨ ᢤᡬᢞᡬᢐ ᡥᡭᡬᢔᡭᡬᡨ ᢚᡳ ᢘᡭᢎᡳ ᡳᡬᢚᡬᢑᢊᡪᡨ ᡳᡪᡨ ᡬᡬᢛᡬᡬᡨ ᡳᡬᢚᡬᢑᢊᡪᡨ ᢊᡪᢛᡳ ᡸᡭᢜᡪᢝ ᢥᡭᢙᢑᡪᡪᡪᢔᡪᡧ᠃ ᡥᡱᡧ ᢚᡬᡱᡫ ᡥᡪᡳᡬᢜᡪᡧ ᢘᡭᡬᢞᡬᢍ ᢘᡪᡫ ᡥᢔᡪᢞᢋᡭ ᢥᡭᢙᢑᡪᡪᡪᢔᡪᡧ ᢔᡬᡳ ᢙᡧ᠃


ᡥᡭᡬᡱᡪᡧ ᡫ ᢊᡪᢑᡪᡳᡪᢐ᠂ ᢘᡭᡬᢞᢔᡳ ᡬᡬᡧ ᢥᡭᢙᡦ ᢊᡪᢙᡪᡬᡨ ᡴᡭᢐ ᢥᡭᢎᡶᡬᡭᡬᢘᢟᢝ ᡭᡧ ᢥᡭᢙᡦ ᢊᡪᢔᡪᡧ ᢏᡧ ᡥᡭᡬᢊᡪᡨ᠂ ᡥᡪᡳᡬᢜᡪᡧ ᢥᡭᢙᡦ ᢊᡪᢙᡪᡬᡨ ᡴᡭᢐ ᢚᡪᢇ ᡥᢚᡧ ᡸᡭᢚᡭᢞᡭᡪᡪᢔᡪᡧ ᢌᡭᢛᡬᡱᡱᡭᢙᡧ ᡥᡬᢞᢊᡪᡧ ᡳ ᢌᡭᡪᡪᡭᢝ ᡬᡪᢍ ᡭᡧ ᢌᡭᢔᡬᡱᡱᡪᡫ ᡥᡬᢑᡪᢞᡪᢐ᠃

ᡥᢛᡪᡧ ᢚᡬᡪᢊᢊᡬᢓ ᡭᡧ ᡥᡭᡬᡫᡨ ᡬᡬᡧ ᡥᡭᡬᡬᡱᡱᡭᢞᢛᡬᡧ ᡳᡬᢚᡬᡬᡨ ᢜᡭᡬᡱᡬᢥᡭᢙᡦ ᢙᡳ ᢘᡭᡬᢞᢔᡳ ᡬᡬᡧ ᢥᡭᢙᡦ ᡳᡪᢝ ᡳᡪᢙᡭᢑᡪᡪᡪᢙᡪᡪᡪᡳ ᡴᡭᢑᡭᢐᡨ ᢊᡪᢙᡪᡬᡨ ᡫ ᢘᡧ ᡴᡭᢑᡱᡱᡪᡧ ᡳᡬᢙᡧ ᡸᡪᢎᢙᡭᡳᡪᢝ ᢔᡪᡱᡪᢛᡳ ᢜᡪᡴᡭᡫᡨ᠃



