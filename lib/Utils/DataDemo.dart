ᢋᡭᡬᢊᡪᢞᡭᢋᡭᡧ ᢘᡭᡬᢞᡭᢊᢔᡪᡧ ᢋᡭᡬᢊᡪᡪᡪᡭᢙᡧ ᡬᡬᡧ ᡯᡬᢊᡪᡨ ᡥᡭᡬᢊᡬᡧ ᢌᡪᡧ ᡳ ᡥᡪᡪᢊᢊᡬᡦ ᢙᡳ ᡳᡪᡬᡬᢙᡪᢇ ᠃ ᡯᡪᢞᡬᡪᡪᡪᡧ ᡥᡭᡬᡪᢙᡭᢝ ᡳᡪᡫᡨ ᢘᡪᡫ ᠂ ᡸᡪᢜᡧ ᡸᡪᢛᡪᡱᡱᡳ ᡳᡪᢝ ᡥᡭᡬᡪᢊᢊᡪᢞᡪᢋᡭ ᢙᡳ ᡥᡪᢊᢊᡪᢔᢊᡪᡨ ᡬᡬᡧ ᡥᡭᡬᡱᡭᢝ ᡥᢞᢊᡬᢛᡳ ᡸᡪᢞᡪᢎᡪᢝ ᡥᡪᢎᢙᡪᡪᡪᢙᡪᡰᡨ ᠃ ᢘᡪᢞᡧ ᡥᢞᡪᢔᢊᡪᡧ ᡬᡪᡪᡬᡨ ᢘᡪᡫ ᠂ ᢘᡪᢊᢞᡫ ᡬᡬᡧ ᢘᡭᡭᢝᡨ ᢘᡪᢋᡭᡧ ᡳ ᡥᡪᢜᡭᢛᡳ ᢔᡭᢚᡬᡪᡪᡳ ᢜᡪᡱᡱᡭᢍᡨ ᢊᡪᢛᡳ ᡥᡪᢑᡪᢉᡨ ᠃ ᡥᡳᡪᢝ ᢘᡪᡫ ᡯᡫ ᡥᡬᢎᡪᢉᡨ ᡥᡳᡪᢝ ᡥᡭᡬᢊᡪᡫ ᡯᡫ ᡸᡭᡱᡫ ᢊᡪᢛᡳ ᡥᡭᡬᢋᡭᢑᡪᢋᡭ ᢙᡳ ᡥᡬᡱᡬᢜᡪᢙᡳ ᢜᡪᢊᡪᡨ ᢋᡭᡬᢞᡭᡰᡨ ᠃ ᢘᡪᢑᡪᢊᡪᡨ ᡸᡭᢞᡪᢉᡨ ᡴᡭᢐ ᢊᡪᡪᢛᡧ ᡸᡭᢞᡪᢉᡨ ᢔᡬᡳ ᢊᡪᢛᡳ ᡳᡪᢙᡭᢑᡪᡪᡪᡳ ᢙᡳ ᠂ ᡛ ᢋᡭᡬᢊᡪᢞᡭᢋᡭᡫ ᢊᡪᡪᢛᡧ ᡸᡭᢞᡪᢉᡨ ᡬᡫ ᢌᡪᢙᡪᢊᢔᡪᡧ ᡯᡫ ᡴᡭᢑᢛᡳ ᢙᡧ ᡒ ᡜ ᢊᡪᢛᡳ ᡳᡬᡦ ᢔᡪᡭᡪᢊᡬᢐ ᢙᡳ ᡳᡪᡧ ᡬᡪᢑᡳᡪᢞᡬᢙᡪᢇ ᠃ ᢈᡭᢜᡳ ᡥᡭᡬᢊᡬᡧ ᢜᡪᢊᡪᡨ ᢚᡪᡬᡬᢑᡱᡱᡪᡧ ᠂ ᢈᡪᢞᡪᢇ ᡭᡧ ᡥᢙᡭᢝ ᡴᡭᢑᡱᡱᡪᡧ ᢊᡪᢝ ᡥᢚᡧ ᡳᡪᡧ ᡴᡭᢚᡪᡪᡪᡳ ᢙᡳ ᡳᡪᡧ ᢔᡬᢊᡬᢝ ᢘᡪᢎᡪᢊᡬᡦ ᡬᡪᢞᢊᡪᡨ ᡬᡬᡧ ᡸᡭᡴᡭᢞᡪᡪᡪᡧ ᡴᡭᢙᡪᢓ ᡫ ᡯᡪᢙᡪᢙᡳ ᡥᡪᡳᢚᡳ ᡥᡬᢞᡪᢙᡪᡬᡨ ᠃ ᢊᡬᢙᡪᡭᡧ ᢊᡪᢑᡪᡧ ᡳ ᢊᡬᢚᡬᢜᡪᢐ ᡴᡭᢐ ᢘᡪᢋᡭᡧ ᡳ ᡥᡭᡬᢛᡪᢋᡭᢑᡭᢑᢊᡪᡨ ᡬᡬᡧ ᢘᡪᡳᢚᡪᡪᡬᡨ ᠂ ᡥᡭᢔᡳ ᢚᡪᢔᡳ ᢔᡬᢇ ᢗᡭᡭᢊᡬᡱᡱᡭᢑᢛᡳ ᢜᡪᢞᡬᡪᡪᡳ ᢙᡳ ᢊᡬᢙᡪᡭᡧ ᢊᡪᢑᡪᡧ ᡳ ᡳᡪᡪᡪᢔᡫ ᢚᡳ ᢜᡪᢊᡪᡨ ᢘᡭᡬᡳᡪᢊᢙᡪᡰᡨ ᠃ ᡸᡪᢜᡧ ᡬᡪᢞᡬᢍ ᢙᡳ ᡳᡪᡧ ᡥᡴᡭᢙᡭᡬᡨ ᢘᡪᡳᡪᡪᡪᢔᡪᡧ ᡥᡭᡪᡪᡭᢝ ᡳᡪᡪᢊᢛᡪᢐ ᢘᡪᡫ ᢘᡪᢞᡳᡪᢋᡭ ᢙᡳ ᡥᡪᡪᢊᢊᡬᡦ ᡬᡬᡧ ᡳᡪᡪᡪᢔᡫ ᡬᡬᡧ ᢔᡬᢞᡭᢋᡭᡧ ᡬᡪᡪᢊᢙᡭᢞᡭᢐ ᡥᢊᡬᢑᡪᡰᡨ ᠃ ᢘᡪᢞᡧ ᢤᡬᡱᡭ ᢘᡭᡪᡪᢑᡪᡪᡪᢚᡫ ᡬᡬᡧ ᡸᡪᡱᡱᡭᢚᡬᡧ ᢔᡭᢞᡪᢇ ᡫ ᡥᡭᢑᡪᡧ ᢚᡭᡪᡪᢑᡪᡱᡱᡭᢑᢙᡪᢇ ᢌᡭᡬᢞᢙᡪᢊᡪᡧ ᠂ ᡯᡪᢔᡭᡧ ᢙᡳ ᢋᡭᡬᢞᡭᢊᢔᡪᡭᡧ ᡭᡧ ᢌᡪᢙᡪᢙᡪᡬᡨ ᡯᡬᡱᡱᡭᢚᡧ ᡥᡭᢚᡬᢝ ᡭᡭᡧ ᡫ ᡴᡭᡬᢝ ᡥᡬᢑᡧ ᢜᡪᢞᡬᢙᡪᢇ ᢜᡭᢍ ᠃ ᢋᡭᡬᢊᡪᢞᡭᢋᡭᡧ ᡥᡭᡬᢊᡬᡧ ᢌᡭᡪᢊᡱᡱᡭᢐ ᡥᡭᡬᢊᡪᡧ ᢙᡳ ᡳᡪᡧ ᡥᡪᡱᡪᡫ ᡴᡭᢑᡳᡪᢚᡳ ᠂ ᢌᡭᡪᢊᡱᡱᡭᢐ ᡥᡭᡬᡪᢙᡭᢔᡭᢙᡪᡧ ᢙᡳ ᡳᡪᡧ ᢜᡪᢊᡪᡨ ᡸᡪᡬᡬᢞᡪᢙᡪᡫ ᡳᡪᡬᡬᢛᡪᡫ ᠃


ᡯᡬᢊᡪᡨ ᡥᡭᢙᡪᢉᡨ ᡬᡪᢊᡪᢑᡫ ᡬᡬᡧ ᡥᡭᢑᡪᡧ ᢏᡭᡬᡬᡳᡪᢞᢚᡬᡧ ᢔᡭᢞᡱᡱᡪᡱᡱᡭᢑᡫ ᡬᡬᡧ ᡥᢋᡭᢙᡧ ᡬᡫ ᢘᡭᡬᢊᢛᡬᢋᡭ ᡥᡭᡬᡫᡨ ᢙᡳ ᠂ ᢋᡭᡬᢊᡪᢞᡭᢋᡭᡫ ᡥᢞᡪᢓ ᡭᡭᡧ ᢘᡪᡱᡱᡳ ᢚᡬᢎᡪᢊᡪᡨ ᡥᡭᡬᢊᡪᡫ ᢗᡭᢎᡴᡭᡬᡬᢛᡳ ᠂ ᢈᡪᡪᡪᢚᡪᡪᡪᡪᡧ ᢘᡪᢞᡧ ᢏᡧ ᡳᡪᢑᡳᡪᢞᡪᡪᡪᡪᡫ ᡥᢋᡭᢙᡧ ᡬᡫ ᢔᡬᡱᡱᡭᡭᡧ ᡥᡬᢔᢋᡭᢑᢛᡳ ᢈᡪᢞᡭᡱᡱᡪᡭᡧ ᠂ ᡛ ᡥᢎᡪᢊᡪᡨ ᡥᢛᡫ ᢚᡬᡱᡫ ᡳᡪᡬᡬᡪᡪᢔᡪᡧ ᢚᡪᢇ ᢘᡳ ᡥᡪᢙᡧ ᢘᡧ ᡯᡪᢝ ᡭᡧ ᢋᡭᡬᡪᢙᡪᢑᡪᡧ ᡥᡪᢑᡪᡪᡭᡪᡪᡳ ᡬᡪᡫ ᡥᡪᢑᡪᢉᡨ ᠂ ᡸᡪᢝᡨ ᢚᡪᡱᡱᡪᡧ ᡥᡪᢑᡫ ᢜᡭᢔᡳ ᡳᡪᢝ ᡯᡫ ᢔᡬᡬᡭᡪᡴᡭᢞᡬᢑᡪᢋᡭ ᡬᡫ ᡸᡭᢞᢙᡭᡧ ᢊᡪᢑᡧ ᡒ ᡜ ᢊᡪᢛᡳ ᡬᡪᡪᢊᢙᡭᢞᡪᡪᡳ ᢙᡳ ᡥᡭᢑᡪᡧ ᢏᡭᡬᡬᡳᡪᢞᢚᡬᡧ ᢗᡭᢑᡴᡭᡬᡬᢙᡪᢐᡨ ᡯᡭᡱᡱᡭᢞᡪᡱᡱᡪᡭᡧ ᡸᡪᢞᡬᡪᡪᢔᡪᡧ ᢜᡭᢍ ᠃ ᢋᡭᡬᢊᡪᢞᡭᢋᡭᡧ ᡥᡭᡬᢊᡬᡧ ᢘᡪᡫ ᢘᡪᢋᡭᢔᡭᢊᢔᡪᡧ ᡥᢚᡧ ᡸᡭᡬᡬᢔᡫ ᢘᡪᢎᡪᡫ ᡥᡪᡱᡱᡭᢑᢛᡪᢉᡨ ᡥᡭᡬᢊᡪᡫ ᠃ ᡥᡪᢞᡬᢊᡬᡧ ᢔᡬᢞᡪᢊᡪᡧ ᢘᡪᢊᡪᢝᡨ ᡥᡪᢑᡫ ᡯᡬᢊᡪᡨ ᡯᡫ ᢘᡪᢋᡭᡧ ᡬᡫ ᢌᡪᡪᡪᢙᡪᡪᡪᡳ ᡥᡭᡬᡫᡨ ᢙᡳ ᡬᡪᢞᢊᡪᢑᡪᢙᡪᢊᡪᡨ ᡥᡪᡬᡬᢐ ᡭᡧ ᢋᡭᡬᢊᡪᢞᡭᢋᡭᡧ ᡥᢊᡪᢚᡫ ᡬᡫ ᢌᡪᡪᡪᢙᡪᡪᡪᢔᡪᡧ ᢜᡭᢎᢔᡬᢇ ᢔᡪᡭᡪᢊᡬᢐ ᡯᡬᢊᡪᡨ ᡯᡪᢑᡭᡬᡬᢊᡪᡭᡧ ᢜᡪᡴᡭᢚᡬᡪᡪᡪᡰᡨ ᠃ ᢋᡭᡬᢞᢊᡬᢝᡨ ᢔᡬᡬᡨ ᡯᡪᢐ ᡸᡪᢝᡨ ᢊᡪᢛᡬᢊᡪᡨ ᡬᡫ ᡯᡫ ᢔᡪᢎᡱᡪᡧ ᠂ ᡸᡭᢙᡭᡬᡬᢙᡪᢐᡨ ᢘᡭᡱᡭᢞᡪᡪᡪᢔᡪᡧ ᡸᡪᢚᡪᢝ ᡭᡧ ᢊᡬᡱᡫ ᡸᡭᡪᢙᡪᡱᡱᡪᡧ ᢙᡳ ᢔᡭᡪᡪᢙᡭᢙᡪᢇ ᢋᡭᡬᢎᡭᡧ ᡴᡭᢜᡪᡧ ᢘᡪᡫ ᢜᡭᢎᢔᡪᡧ ᠃ ᢋᡭᡬᢊᡪᡪᡪᡭᢙᡧ ᡬᡬᡧ ᢋᡭᡬᢊᡪᢞᡭᢋᡭᡧ ᡥᡭᡬᢊᡬᡧ ᢏᡪᡲ ᢔᡪᡬᡬᡧ ᡥᢝᡨ ᢙᡳ ᢌᡭᢞᢙᡪᡪᡪᢔᡪᡧ ᡳᡪᡬᡬᢇᡨ ᡒ


