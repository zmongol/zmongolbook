ᢈᡭᢞᡳᡪᡨ ᠂ ᡸᡪᢔᡭᡧ ᡳᡪᡪᡪᢔᡫ
ᡸᡪᢔᡭᡧ ᡳᡪᡪᡪᢔᡫ ᢌᡬᡱᡳ ᡥᡪᡪᢊᢊᡬᡦ ᡬᡬᡧ ᡥᢞᢊᡬᢑᡪᢊᢚᡫ ᡳᡪᡬᡬᡪᡪᢔᡪᡧ ᠃ ᡯᡬᢞᡭᡱᡱᡳ ᡳᡪᡧ ᡥᢋᡭᢞᢚᡳ ᠂ ᡯᡪᢎᡳᡪᡬᡬᢙᡪᢐᡨ ᡥᡪᢑᡪᡪᡭᡪᡪᡳ ᢙᡳ ᢜᡪᢇ ᡥᡭᢙᡧ ᡬᡬᡧ ᡯᡪᢎᡪᢉᡨ ᢔᡬᢇ ᡯᡪᡬᡬᡱᡱᡭᢙᡪᢇ ᢚᡳ ᠂ ᡥᡪᢑᡬᡫᡨ ᢔᡭᢞᡭᡪᡪᢚᡫ ᡬᡫ ᡬᡪᡪᢊᡱᡪᡪᡪᡳ ᡥᡭᡬᡫᡨ ᢙᡳ ᢘᡪᡱᡱᡭᡧ ᡳ ᡥᡭᡬᡪᢊᢊᡪᡨ ᡯᡫ ᡥᡭᡬᡪᢙᡭᢝ ᡳᡪᡬᡬᡪᡪᢔᡪᡧ ᠃ ᢘᡪᡭᢊᡪᡨ ᡬᡬᡧ ᢊᡬᢚᡬᢜᡪᢐ ᡭᡧ ᡳᡪᡪᡪᢔᡫ ᡴᡭᢑᡪᡪᡭᢝ ᡥᡭᡬᢑᡬᢊᡪᢝ ᢔᡪᡬᡬᡪᡪᡪᡧ ᢜᡪᢞᡬᢙᡪᢇ ᠃ ᡥᡭᡪᢚᡪᡱᡱᡪᡬᡬᢑᡪᡧ ᢌᡭᡪᢊᡱᡱᡭᢐ ᡭᡧ ᢘᡪᡭᢊᡪᡨ ᡬᡫ ᢘᡭᢞᡪᢔᡪᡪᡳ ᡥᡭᡬᡫᡨ ᢙᡳ ᠂ ᢔᡪᡭᡪᢊᡬᢋᡭᡫ ᡯᡫ ᢜᡪᢊᡪᡨ ᡴᡭᢙᡭᢑᡬᢜᡪᡧ ᢘᡪᡫ ᡳᡪᡬᡬᡪᡪᢔᡪᡧ ᠃ ᡥᡭᢑᡪᡱᡱᡪᡧ ᡴᡭᢙᡭᡪᡬᡨ ᡭᡧ ᢘᡪᡬᡬᢑᡪᢑᢙᡭᡱᡱᡪᡧ ᡫ ᢜᡪᢞᡬᡪᡪᡳ ᢙᡳ ᠂ ᡥᡪᢊᢊᡪᡨ ᡥᡪᢎᡭᡱᡱᡭᢑᡪᡪᡬᡨ ᡭᡧ ᡸᡭᢑᢛᡬᡧ ᡳᡪᢔᡬᢝ ᡸᡪᢞᡪᡪᡪᢙᡪᡧ ᠂ ᡬᡪᡱᡱᡭᡧ ᢌᡭᢙᡭᡧ ᡳ ᢘᡪᡬᡬᡧ ᡫ ᢘᡭᢞᡪᢔᡪᡪᡳ ᢙᡳ ᠂ ᡸᡪᢑᢇᡨ ᢋᡭᡬᡪᡬᡨ ᡭᡧ ᡥᡭᡬᢊᡪᡪᢚᡧ ᢚᡬᢞᡪᡫ ᡥᡭᡬᢛᡪᢊᢙᡪᢛᡳ ᠂ ᡛ ᢌᡬᡱᡳ ᡯᡭᢙᡭᢇ ᡭᡧ ᢈᡪᢛᡪᢝ ᢔᡬᢞᡭᡫ ᡬᡫ ᡴᡭᢞᡪᡪᡪᡧ ᢈᡭᢜᡭᡪᡪᢔᡪᡧ ᢚᡳ ᡴᡭᡳ ᡥᡭᡬᡬᡨ ᡜ ᢊᡪᢛᡳ ᡥᡭᡪᢊᢔᡬᡪᡪᡳ ᢙᡳ ᢈᡪᢑᢙᡪᡧ ᡴᡭᢗᡭᡪᡪᢙᡳ ᡬᡬᡧ ᢈᡪᢔᡬᡱᡱᡭᡧ ᡯᡬᢑᡴᡭᢔᡳ ᢘᡭᡬᢋᡭᢊᡪᡫ ᡥᡪᢎᢙᡪᡪᡪᢙᡪᡰᡨ ᠃ ᢏᡬᢊᢙᡪᡧ ᡸᡪᡱᡱᡪᡧ ᡳ ᡥᡭᡬᢑᡪᢙᡪᡴᡭᢞᡫ ᡸᡪᢙᡭᡧ ᡸᡪᢔᡴᡭᡳ ᢘᡪᢎᡪᢉᡨ ᡬᡫ ᢚᡬᡪᡬᡨ ᡭᡧ ᡸᡪᡱᡱᡪᡧ ᢙᡳ ᡥᢞᢋᡭᢋᡭ ᡥᡭᡬᡫᡨ ᠂ ᢌᡭᡪᢊᡱᡱᡭᢐ ᡭᡧ ᡥᡪᢑᢙᡪᡧ ᡬᡪᢔᡪᢇ ᢘᡪᡭᢊᡪᡧ ᡥᢚᡧ ᡸᡪᢔᡭᡪᡪᢙᡪᡪᡪᢔᡪᡧ ᢊᡪᢛᡳ ᢜᡪᢞᡬᢙᡪᢇ ᡫ ᡯᡫ ᢚᡪᢊᡪᢛᡬᢑᡪᢛᡳ ᡳᡪᡬᡬᡰᡨ ᠃


ᡥᡭᡬᡪᢊᢊᡪᢞᡪᡳ ᡥᢞᢙᡧ ᡬᡬᡧ ᡥᡬᡱᡪᢇ ᢘᡭᢞᡪᢑᡪᢐ ᡥᡭᡬᢔᡴᡭᢞᡫ ᡬᡬᡧ ᢊᡪᡭᢊᡪᡭᡧ ᡭᡭᡧ ᡭᡧ ᢔᡭᢞᡭᢑᢉᡨ ᡬᡫ ᡯᡭᡬᢑᡭᢊᡪᢑᡪᢙᡪᡬᡨ ᢊᡪᢛᡳ ᡸᡪᢔᡭᡧ ᡳᡪᡪᡪᢔᡫ ᢙᡭᡬᡪᢊᡱᡪᢛᡳ ᡳᡪᡬᡬᡪᡪᢔᡪᡧ ᠃ ᡯᡪᡬᡬᢊᡪᢍ ᡭᡧ ᡥᡪᡪᢊᢊᡬᡦ ᢙᡳ ᢈᡭᢜᡳ ᡥᡭᡬᢊᡬᡧ ᢚᡭᡪᡪᢑᡪᢞᡪᢙᡪᢇ ᡯᡫ ᢔᡭᡱᡬᡪᡪᡪᡧ ᡯᡬᢊᡪᡧ ᢜᡭᢔᡭᡧ ᡳᡬᢑᡧ ᠃ ᢘᡪᢊᡪᢝᡨ ᢘᡭᡭᢝᡨ ᡥᡪᡪᢊᢊᡬᡦ ᡬᡬᡧ ᡸᡪᢝᡨ ᢋᡭᡬᡭᢚᡭᡭᢐ ᡭᡧ ᡯᡬᢙᡳ ᠂ ᢌᡪᡧ ᡳ ᡥᡪᡪᢊᢊᡬᡦ ᡬᡬᡧ ᢚᡪᢚᡪᡬᡨ ᢔᡬᡬᡨ ᢈᡭᢜᡳ ᡥᡭᡬᢊᡬᡭᡧ ᡭᡧ ᡳᡪᡫᡨ ᢘᡪᢊᡪᢝᡨ ᡥᢚᡧ ᢔᡪᢑᢙᡪᢇ ᡥᡭᡬᢊᡪᡫ ᡳᡪᡬᡬᡪᡪᢔᡪᡧ ᠃ ᡸᡪᢔᡭᡧ ᡳᡪᡪᡪᢔᡫ ᢜᡪᢊᡪᡨ ᢔᡪᡪᢙᡭᢞᡭᡰᡨ ᠂ ᡸᡪᢎᡭᢇ ᡭᡧ ᢜᡪᢊᡪᡨ ᢘᡪᢞᡭᡪᢊᡱᡱᡭᡬᡬᢑᡪᢐ ᡭᡧ ᢜᡭᢔᡳ ᡳᡪᢝ ᢔᡬᢞᡭᢋᡭᡧ ᡸᡪᡪᢙᡭᡰᡨ ᠃ ᡥᡬᡱᡪᢇ ᢘᡭᢞᡪᢑᡪᢐ ᡭᡧ ᢋᡭᡬᢚᡭᡧ ᡥᡪᡱᡱᡭᡳ ᢜᡪᢊᡪᡨ ᡳᡬᢑᡧ ᠂ ᡸᡪᢙᡪᡧ ᡳ ᢘᡭᡭᢝᡨ ᡥᢚᡧ ᡥᡪᢎᡬᢑᡪᢙᡪᢇ ᡸᡪᡪᡪᢙᡧ ᢚᡪᢚᡪᡬᡨ ᢔᡬᡬᡨ ᡥᡭᢞᡱᡱᡭᡱᡱᡪᡭᡧ ᢏᡧ ᡳᡪᡬᡬᡰᡨ ᠃ ᢔᡪᡪᢙᡭᢞᡭᡪᡪᢔᡪᡧ ᡸᡪ ᡳᡪᡪᡪᢔᡫ ᢘᡪᢊᢞᡫ ᡬᡬᡧ ᢊᡬᢛᡪᡱᡱᡪᢝ ᡥᢚᡧ ᢊᡪᢝ ᡭᡧ ᡥᡪᡪᡪᡪᢎᡪᡭᡧ ᡫ ᢘᡪᡱᡱᡭᢙᡪᢙᡪᢇ ᠃ ᡥᢙᡭᢝ ᡯᡫ ᢊᡪᢑᡧ ᡳᡪᡧ ᢚᡭᢑᢚᡪᢞᡪᢙᡪᢐᡨ ᡥᡪᡱᡱᡭᡳ ᢜᡭᢔᡭᡧ ᡬᡫ ᡥᡭᡬᢋᡭᢑᡪᡧ ᠂ ᡥᡭᢞᡭᡫ ᡯᡫ ᢊᡪᢝ ᡭᡧ ᡥᡪᡪᡪᡪᢎᡪᡭᡧ ᡫ ᢘᡪᡱᡱᡭᢙᡪᡧ ᢊᡪᢝ ᢘᡳ ᡳᡪᡧ ᡸᡭᡱᡱᡭᢑᡧ ᢚᡪᡫ ᡳᡪᢛᡪᡱᡱᡪᡰᡨ ᠃


ᡥᡭᢞᢚᡬᢎᡪᡪᡪᡧ ᢌᡪᡧ ᡳ ᡥᡪᡪᢊᢊᡬᡦ ᡬᡬᡧ ᢊᡬᡧ ᢚᡭᡪᡪᢑᡪᢞᡪᢛᡳ ᠂ ᡸᡪᢔᡭᡧ ᡳᡪᡪᡪᢔᡫ ᢘᡪᡫ ᡯᡪᢞᢊᡬᡪᡪᢔᡪᡧ ᢜᡭᢍ ᡥᡨ ᡒ ᡥᡪᢞᡬᢊᡬᡧ ᢔᡬᢞᡪᢊᡪᡧ ᢘᡪᢊᡪᢝᡨ ᡳᡪᡪᡪᢔᡫ ᡬᡬᡧ ᢜᡪᢞᡬᡪᡪᢔᡪᡧ ᡯᡬᢊᡪᡨ ᡥᡭᡬᢊᡪᡨ ᡳᡬᢙᡪᡧ ᡫ ᢜᡪᢊᡪᡨ ᢔᡭᢚᡬᡱᡱᡪᢐᡨ ᠃ ᡛ ᡳᡬᡦ ᢘᡪᢞᡧ ᡥᡭᡬᡫᡨ ᢙᡳ ᢊᡪᢞᡳᡪᡨ ᡥᡭᢑᡪᡧ ᡥᡪᢞᡳᡬᡧ ᡯᡬᡱᡱᡭᢚᡧ ᡥᡪᢎᡪᢞᡪᢇ ᡭᡭᡧ ᡫ ᡬᡭᡪᡪᢔᡭᡱᡱᡪᡪᡪᢔᡪᡧ ᡥᡭᡬᢊᡪᡫ ᡴᡭᢐ ᠂ ᡥᡭᢙᡳ ᢌᡭᡪᢊᡱᡱᡭᢐ ᡭᡧ ᡥᡭᡬᢝᡨ ᢔᡪᡪᢙᡳ ᡥᡭᢑᡪᡧ ᡥᡪᢞᡳᡬᡧ ᡥᡭᡬᢞᡪᢛᡬᢊᢔᡪᡧ ᢜᡭᢍ ᡴᡭᢐ ᡭᡳ ᡓ ᡜ ᠂ ᡸᡪᢔᡭᡧ ᡳᡪᡪᡪᢔᡫ ᡬᡬᡧ ᡸᡭᡱᡱᡭᢑᡪᡫ ᡬᡬᡧ ᡥᡭᡬᡪᢊᢊᡪᡨ ᡥᢞᡬᢜᡪᡪᢙᡭᡧ ᠂ ᡸᡪᢚᡪᢝ ᡫ ᡯᡫ ᢘᡪᡱᡱᡪᡧ ᡴᡭᡬᢑᡬᢜᡪᡧ ᡯᡬᢑᡴᡭᢔᡳ ᡴᡭᡬᢎᡴᡭᢞᡬᢋᡭ ᡬᡫ ᡥᡭᢑᢛᡳ ᡸᡪᢞᡪᡪᡪᢔᡪᡧ ᢚᡳ ᢊᡪᡧ ᢚᡳ ᡥᡭᡬᢊᡪᡨ ᢘᡭᡭᡱᡱᡪᢞᡭᡪᡪᢔᡪᡧ ᡥᡭᡬᢊᡪᡫ ᠃ ᢚᡬᢎᡪᢊᡪᡨ ᡥᡪᡱᡬᢝ ᡥᡭᡬᢊᡪᡫ ᡯᡪᢍ ᡬᡬᢍ ᡭᡧ ᢘᡭᢎᢙᡧ ᢚᡬᡪᡬᡨ ᡥᡭᡬᡱᡪᡪᢚᡫ ᡸᡪᡬᡬᢝᡨ ᡬᡫ ᡥᡭᡪᡪᡪᡱᡱᡪᢞᡪᡪᡪᢔᡪᡧ ᢜᡭᢍ ᡥᡨ ᠃ ᡥᡭᡬᡪᢙᡭᢔᡭᢙᡪᡧ ᡬᡬᡪᡧ ᢊᡪᡳᡪᢐ ᡥᡭᡬᢞᡳ ᡬᡬᢞᡭᢊᡪᡨ ᡯᡫ ᡥᡭᡬᡪᢙᡪᢑᢛᡪᢛᡳ ᠂ ᢔᡭᢞᡭᡪᡪᢚᡫ ᡳᡪᡧ ᢊᡪᡳᡪᢐ ᡥᡭᡬᡱᡭᢚᡬᡧ ᡥᢑᡬᢊᡪᡨ ᡯᡫ ᡥᢎᢙᡪᢞᡪᢙᡪᡬᡨ ᡳᡪᡪᡪᢔᡫ ᢋᡭᡬᢎᡭᡧ ᢋᡭᡬᢊᡪᢞᡭᢋᡭᡫ ᢜᡭᢎᢔᡪᡧ ᠃



ᢘᡭᡬᢞᡳᡪᡨ ᠂ ᡥᡪᢑᢙᡪᡪᢚᡬᢑᡪᡱᡱᡳ ᡳᡪᡪᡪᢔᡫ
ᡥᡭᡬᡪᢙᡭᢝ ᢘᡭᢎᢙᡪᢙᡳ ᡬᡬᡧ ᡸᡭᢜᡪᢙᡭᡱᡱᡪᢝ ᡥᡭᡧ ᡳ ᢊᡪᢑᡧ ᡳᡬᢚᡬᡬᡨ ᡫ ᡥᡪᢑᢙᡪᡪᢚᡬᢑᡪᡱᡱᡳ ᡳᡪᡪᡪᢔᡫ ᡳᡬᢙᡪᡧ ᢙᡳ ᡬᡬᡱᡱᡪᡪᡪᢔᡪᡧ ᢜᡭᢍ ᠃ ᡥᡭᡬᡪᢙᡭᢝ ᡴᡭᢛᡪᡱᡱᡪᡫ ᡳᡪᡫᡨ ᢘᡪᡫ ᠂ ᢘᡪᢞᡪᡪᡪᡧ ᢌᡭᡭᢎᡬᡪᢊᡱᡱᡪᡧ ᡸᡭᢑᡴᡭᡱᡱᡪᢙᡳ ᡸᡭᢔᡬᡱᡱᡭᡧ ᡳ ᢔᡭᡬᢞᢑᡬᡬᡨ ᡥᢝᡨ ᢋᡭᡬᢎᡭᡧ ᢔᡭᢞᡱᡱᡪᡱᡱᡭᢑᡫ ᡬᡬᡧ ᢋᡭᡬᢞᡬᢜᡪᡰᡨ ᢋᡭᡬᡪᢙᡪᢑᡪᡧ ᡥᡪᢑᡪᡪᡭᡪᡪᡳ ᢙᡳ ᠂ ᢌᡭᢞᡬᡧ ᡳ ᡯᡬᢞᡭᡱᡱᡭᡧ ᢘᡪᢊᡪᢝᡨ ᡸᡭᡱᡫ ᡥᡭᡬᡳᢚᡬᢙᡪᡬᡨ ᢔᡬᢑᡫ ᡬᡬᡧ ᢔᡪᡬᡬᡧ ᡥᢝᡨ ᡬᡫ ᡥᡭᡬᡳᡪᢝ ᡭᡧ ᡥᢞᢊᡪᡨ ᡥᡭᡬᢊᡪᡫ ᢔᡪᡱᡪᡪᡪᢙᡪᡱᡱᡭᢑᡭᡰᡨ ᠃ ᢔᡪᢑᢊᡬᡧ ᡴᡭᢞᡭᡱᡱᡪᡧ ᢘᡪᡫ ᡥᢙᡭᢝ ᡴᡭᢑᡱᡱᡪᡧ ᢏᡧ ᢚᡪᡪᢊᢊᡪᢝ ᡥᡭᡬᡪᢊᢊᡪᡨ ᡬᡬᡧ ᢘᡪᡪᡬᡨ ᡥᡭᢞᢙᡳ ᢋᡭᡬᢞᢍᡨ ᡬᡬᡧ ᡥᡪᢊᢊᡪᢝ ᡫ ᡯᡫ ᡬᡪᢙᡪᡱᡱᡪᡫ ᢘᡪᢞᡳᡪᢋᡭᢑᢛᡳ ᡥᡪᢑᡪᡪᡭᢙᡪᢇ ᠃ ᡥᢝᡨ ᡸᡭᡪᢊᡱᡱᡭᢝ ᢘᡪᡱᡱᡭᡧ ᡳ ᡥᡭᡬᡪᢊᢊᡪᡨ ᡯᡫ ᡸᡪᢎᡬᢉᡨ ᢔᡪᡫ ᡥᡭᡬᢊᡪᡫ ᢘᡭᡱᡱᡭᢑᢙᡪᡰᡨ ᠃


ᡯᡬᢊᡪᡨ ᡥᡭᢙᡪᢉᡨ ᢊᡬᢚᡬᢜᡪᢐ ᢘᡪᢊᡪᢝᡨ ᢋᡭᡬᢎᡭᡧ ᡳ ᢘᡭᡬᢞᢔᡳ ᡳᡪᡬᡬᢙᡪᢐ ᡫ ᡬᡬᢞᡭᡪᡪᢑᡪᡪᡪᡳ ᢘᡪᢔᡪᡪᡪᢐ ᡳᡬᢙᡪᡧ ᢙᡳ ᢘᡭᢔᡬᢜᡪᢐᡨ ᠃ ᡳᡬᡦ ᢔᡭᢞᡭᢑᢉᡨ ᡬᡬᡧ ᢊᡪᢔᡬᢋᡭᡧ ᡴᡭᢑᡪᡪᡭᢝ ᡸᡪᢎᡭᢇ ᢘᡭᡬᢞᡭᢋᡭᡧ ᢙᡳ ᢌᡬᡱᡳ ᡴᡭᡬᢙᡭᢊᡪᢐ ᡫ ᡥᡭᡪᢊᢔᡬᡪᡪᢔᡪᡧ ᢜᡭᢍ ᠃

ᡛ ᡸᡪᡱᡱᡪᢑᢉᡨ ᡯᡪᢊᡪᢊᡪᢊᢙᡪᢛᡳ ᢊᡪᢙᡪᢔᡳ ᡥᡭᢞᡭᢛᡳ ᡥᡬᢞᡪᡳᡪᡨ

ᡸᡪᡱᡱᡪᢓ ᢚᡪᢇ ᡭᡧ ᢘᡪᢞᡪᢉᡨ ᡸᡪᢎᡪᢝ ᡥᡭᡬᢛᡪᢊᢙᡪᡳᡪᡨ ᠁ ᡜ

ᡛ ᡸᡧ ᡸᡧ ᡸᡧ ᠂ ᡥᡱᡧ ᢌᡪᡱᡱᡳ ᢋᡭᡬᡳ ᡯᡪᢎᡪᡬᡫ ᡳᡬᢚᡬᢊᢔᡪᡧ ᡳᡪᡬᡬᢛᡳ ᢔᡬᢙᡧ ᡒ ᢘᡭᡬᢋᡭᢞᡪᡪᡬᡨ ᡸᡭᡳᡬᡦ ᡥᡭᡬᢊᢋᡭᡫᡨ ᢊᡪᢛᡳ ᡸᡪᢙᡪᡱᡱᡪᡱᡪᢙᡪᢐᡨ ᡥᡬᡱᡬᢜᡪᢋᡭ ᢙᡳ ᢌᡪᡬᡬᢛᡬᢊᡬᢝ ᢊᡪᢝ ᡭᡧ ᢊᡪᢑᢙᡪᢞᢊᡪᡫ ᢘᡭᡱᡱᡭᢔᢇᡨ ᢋᡭᡬᢙᡪᢑᢋᡭ ᢜᡭᢎᢔᡬᢇ ᢔᡪᡱᡪᡪᡪᢙᡪᡰᡨ ᠁ ᡜ


ᡥᡪᢑᢙᡪᡪᢚᡬᢑᡪᡱᡱᡳ ᡳᡪᡪᡪᢔᡫ ᢙᡳ ᡳᡪᢔᡧ ᡯᡬᢊᡪᡨ ᡥᡭᢑᡪᡧ ᢋᡭᡬᢎᡭᡧ ᢌᡪᢙᡪᢙᡪᡬᡨ ᡥᡭᡬᢊᡪᡫ ᡥᡭᢎᡭᡪᡪᢔᡬᢐ ᡳᡪᡬᡬᢙᡪᢇ ᠃ ᢔᡪᢞᡧ ᢊᡪᡬᡬᢊᢔᡪᡧ ᡯᡬᢊᡪᡨ ᡥᡭᡬᢙᡪᢔᡫ ᡛ ᡥᡪᡴᡭ ᠂ ᡥᢛᡫ ᡜ ᡥᢚᡧ ᡥᡭᡬᢊᡪᢝᡨ ᡥᡭᡬᢊᡪᡨ ᢌᡪᢙᡪᢊᡪᡨ ᡥᡭᡬᢊᡪᡫ ᡯᡬᢑᢇᡨ ᢋᡭᡬᡳ ᡳᡪᡧ ᢘᡪᡳᡪᢞᡬᢛᡳ ᢔᡭᢞᡱᡱᡪᡱᡱᡭᢑᡫ ᡬᡬᡧ ᡯᡪᢍ ᢋᡭᡬᢞᡬᢜᡪᡰᡨ ᡥᡪᢑᡪᡪᡭᢑᡪᢛᡪᡫ ᠃ ᢘᡪᢊᢞᡫ ᡬᡬᡧ ᡸᡪᢜᡪᡱᡱᡪᡰᡨ ᢘᡭᡱᡭᢞᡪᢙᡪᢐᡨ ᢊᡪᡬᡬᢊᢔᡪᡧ ᢔᡪᢞᡧ ᡬᡫ ᡥᡭᡬᢛᡪᢊᡪᡭᡧ ᠂ ᢋᡭᡬᡳ ᡯᡫ ᡛ ᢔᡪᢞᡧ ᡜ ᢊᡪᢛᡳ ᢋᡭᡬᢚᡭᢙᡪᡫ ᡸᡪᢔᢊᡬᢞᡳᡪᡨ ᠃ ᡛ ᢔᡪᢞᡧ ᡜ ᠂ ᡥᡱᡧ ᡴᡭᢐ ᢜᡬᢞᢙᡬᡪᢚᡳ ᡬᡬᡧ ᡸᡪᢎᡭᢇ ᡥᡭᡪᡪᡭᢝ ᡬᡪᢞᡬᢎᢙᡭᢇ ᡥᡭᡬᢋᡭᢑᡪᡴᡭᢞᡫ ᠂ ᡥᡱᡪᢋᡭ ᡥᡪᡱᡱᡭᡳ ᡴᡭᡬᢙᡭᢊᡪᢐ ᡫ ᢌᡬᡱᡳ ᢋᡭᡬᡳ ᢏᡧ ᡬᡬᢞᡭᡪᡪᢑᡪᡪᡪᢔᡪᡧ ᢜᡭᢍ ᢊᡪᢛᡳ ᡳᡪᡪᡪᢔᡫ ᢌᡬᡱᡫ ᡥᡭᢎᡭᡪᡪᢔᡬᡳᡪᡨ ᠃


ᢘᡪᡬᡬᢎᡳ ᡥᡨ ᡒ ᢊᡪᢙᡭᡬᡬᡳᡪᢝ ᡸᡪᢎᡭᢇ ᡥᡭᡪᡪᡭᢝ ᡬᡪᢞᡬᢎᢙᡭᢇ ᡥᡭᡬᢋᡭᢑᡪᡴᡭᢞᡫ ᡴᡭᢑᡳᡪᢚᡳ ᠂ ᡥᡭᡬᡫ ᡥᡭᢑᡪᡧ ᢌᡭᡪᢊᡱᡱᡭᢐ ᡥᡭᡬᢞᡪᢓ ᡭᡧ ᢜᡬᢞᢙᡬᡪᢚᡳ ᡬᡫ ᢘᡪᡱᡬᡪᡪᢔᡪᡧ ᢘᡭᢎᡳ ᡥᡪᢑᡪᡪᡭᢍ ᡴᡭᢑᡭᡰᡨ ᠃ ᡥᡭᢞᡭᢓ ᢋᡭᡬᢎᡭᡧ ᡳ ᡬᡪᡪᡬᡨ ᢚᡬᡱᡪᢝ ᡫ ᢔᡭᢞᡪᡪᡳ ᡥᢚᡧ ᡥᢎᡭᡰᡨ ᡥᡭᢞᢙᡭᢓ ᢋᡭᡬᢎᡭᡧ ᡳ ᢜᡭᢔᡳ ᡬᡬᢞᡭᢍ ᡫ ᢌᡪᢙᡧ ᢊᡪᢔᡪᡧ ᡳᡪᡪᡪᢔᡫ ᡬᡬᡧ ᡬᡬᡪᡧ ᢔᡭᢞᡱᡱᡪᢐ ᡫ ᡥᡪᢙᡧ ᢙᡳ ᢘᡪᢎᢙᡪᢊᢑᡪᢋᡭ ᢙᡳ ᡥᡭᢞᢎᡪᢙᡪᡫ ᡳᡪᡬᡬᡰᡨ ᠃



