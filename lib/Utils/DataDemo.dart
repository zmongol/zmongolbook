ᢔᡭᢞᡭᡪᡪᢚᡫ ᡬᡬᡧ ᡸᡭᢞᡪᢐ ᡭᡧ ᢊᡪᢔᡬᢋᡭᡧ ᡳᡬᢔᡫ ᡴᡭᢑᡪᡪᡭᢝ ᠂ ᡬᡪᢊᡬᢞᡭᢐ ᢈᡭᢟᡪᡫ ᡯᡪᢎᡪᡬᡫ ᢘᡪᡱᡬᢙᡪᢇ ᡥᡭᡬᢊᡪᡫ ᠃ ᡯᡪᢎᡪᡪᡪᡧ ᡳᡪᡫᡨ ᢘᡪᡫ ᢚᡳ ᢊᡪᢙᡪᡬᡬᢛᡳ ᡥᡪᢑᡪᡪᡭᢙᡪᢇ ᡴᡭᢑᡪᡪᡭᢝ ᢔᡭᡬᢞᢑᡬᡬᡨ ᡸᡪᢞᡪᡪᡪᢙᡪᢙᡪᢇ ᠃ ᢋᡭᡬᢙᡪᢊᡪᡨ ᡬᡬᡧ ᢊᡪᡭᢊᡪᡭᡧ ᡭᡭᡧ ᡫ ᢘᡪᢎᡪᡫ ᢘᡭᡱᡱᡭᡪᡪᡳ ᡥᡭᡬᢊᡪᡫ ᠂ ᢘᡭᡬᢞᡳᡪᢑᢛᡬᡧ ᡥᡪᢑᡪᡪᡭᢉᡨ ᡳᡪᢝ ᡥᡪᡱᡱᡪᢛᡬᡱᡱᡭᡪᡪᡪᡧ ᢋᡭᡬᢙᡪᢑᢛᡳ ᠂ ᡸᡪᢜᡧ ᡯᡬᢊᡪᡨ ᡥᡪᢔᡪᢝ ᡭᡧ ᢈᡭᢙᡭᢎᢛᡫ ᡳᡪᢝ ᡬᡭᢑᡱᡱᡪᡪᡪᡳ ᢙᡳ ᡴᡭᢞᡳ ᡬᡬᢞᡭᢊᡪᡨ ᢌᡬᡱᡫ ᡴᡭᢐ ᡴᡭᢐ ᢚᡭᢊᡬᢑᡪᢛᡳ ᠂ ᡬᡪᢊᡬᢞᡭᢐ ᢈᡭᢟᡪᡫ ᢔᡪᡱᡪᢉᡨ ᢔᡪᡭᡪᢊᡬᢐ ᢔᡪᡬᡬᡪᡪᡪᡧ ᡥᡭᡬᡫᡨ ᡸᡪᢜᡧ ᡯᡬᢊᡪᡨ ᢘᡭᢑᡭᡱᡱᡪᡫ ᢘᡭᢊᡬᢛᡳ ᢌᡪᢙᡪᡰᡨ ᠃ ᡥᡱᡧ ᡴᡭᢐ ᡸᡪᡱᡱᡭᢚᡬᡧ ᡥᡭᡬᡫᡨ ᡬᡬᡧ ᡸᡪᡱᡱᡪᡧ ᡳ ᢔᡭᢜᡭᢞᡪᡪᡪᢐ ᢔᡬᢇ ᡥᡪᡱᡱᡭᡳ ᡳᡪᡬᡬᢐᡨ ᠃ ᡬᡪᢊᡬᢞᡭᢐ ᢈᡭᢟᡪᡫ ᡥᡪᢑᡬᡫᡨ ᢊᡪᡭᢊᡪᡭᡧ ᡭᡭᡧ ᡫ ᡥᡭᡬᢛᡪᢛᡳ ᢚᡬᢙᡪᢙᡪᢇ ᡥᡭᡬᢊᡪᡫ ᠃ ᡸᡭᢑᡪᡱᡱᡪᡫ ᡳᡪᢝ ᢤᡬᡱᡭ ᡥᡭᡬᢛᡪᢛᡳ ᢘᡪᡱᡱᡭᡳ ᢊᡪᡪᢊᢔᡬᡧ ᡸᡭᢞᢙᡭᡪᡪᡪᡧ ᡴᡭᢚᡪᢉᡨ ᡴᡭᢞᡳ ᢋᡭᡬᡳ ᡬᡫ ᠂ ᡥᡪᢔᡪᢝ ᡭᡧ ᡥᢎᡭᡰᡨ ᢈᡭᢛᡭᡬᡬᢙᡪᢐᡨ ᡬᡭᡪᡪᢔᡭᡱᡱᡪᢛᡳ ᠂ ᡛ ᢚᡫ ᡴᡭᡬᢊᡪᡨ ᡕ ᢘᡪᢞᡬᢋᡭᢑᡪᢊᢚᡫ ᡴᡭᡬᢊᡪᡨ ᡖ ᡬᡬᡧ ᢋᡭᡬᡳ ᢜᡭᢍ ᡭᡳ ᡓ ᡜ ᢊᡪᢛᡳ ᡬᡪᡪᢊᢙᡭᡧ ᡥᡪᢔᡪᡱᡱᡭᡳᡪᡨ ᠃ ᡛ ᡳᡬᢔᡫ ᠂ ᢌᡬᡱᡳ ᡥᡪᢛᡪᡫ ᢘᡪᢎᢞᡬᡧ ᢊᡪᢙᡪᡬᡨ ᡜ ᢊᡪᢛᡳ ᢚᡪᡪᡪᡪᢝ ᢋᡭᡬᡳ ᡯᡭᢎᡭᡪᡪᡪᡧ ᡸᡪᢞᡬᡱᡱᡭᢑᡳᡪᡨ ᠃ ᡛ ᡴᡭᡬᢊᡪᡨ ᡬᡬᡧ ᢋᡭᡬᡳ ᡳᡬᢔᡫ ᡴᡭᢐ ᡴᡭᡬᢊᢔᡧ ᡳᡪᡧ ᡸᡪᡳᢚᡬᡱᡱᡪᡭᡧ ᡳᡬᢚᡬᡬᡨ ᡬᡬᡪᡧ ᡥᡭᡬᢛᡧ ᡒ ᡸᡭᡱᡫ ᡸᡭᢞᡪᢉᡨ ᡸᡪᢞᡬᡱᡱᡭᢑᡭᡪᡪᢔᡪᡧ ᡥᡪᢛᡪᡫ ᢚᡬᡱᡫ ᡥᡭᢙᡳ ᢋᡭᡬᢙᡪᢊᡪᡨ ᡬᡬᡧ ᢋᡭᡬᡬᢙᡪᡧ ᢙᡳ ᢋᡭᡬᢑᢙᡪᢛᡳ ᡳᡪᡬᡬᢛᡳ ᢌᡪᢙᡪᡰᡨ ᡜ ᠃


