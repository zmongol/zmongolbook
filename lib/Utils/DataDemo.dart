ᡛ ᡥᢝᡨ ᡯᡫ ᢔᡭᡬᢞᢑᡬᡬᡨ ᠂ ᡴᡭᡬᢔᡪᢋᡭᡫ ᡯᡫ ᢈᡭᡳᡨ ᢊᡪᢛᡳ ᡥᡱᡧ ᡸᡭᢜᡪᢝ ᡳᡪᡪᡪᢔᡫ ᡬᡫ ᢊᡪᢑᡪᡰᡨ ᢙᢟᡦ ᡒ ᡜ ᡬᢟᡪᡬᡨ ᡳᡪᢜᡪᢝ ᡳᡪᡪᡪᢔᡫ ᢘᡪᢎᡬᢝ ᡬᡬᡱᡱᡪᢙᡪᢇ ᠃ ᢘᡪᡪᢊᢚᡪᢋᡭᢊᡪᡧ ᢔᡪᡬᡬᡪᡪᡪᡧ ᡥᢝᡨ ᢋᡭᡬᢎᡭᡧ ᡳ ᡳᡪᡫᡨ ᡬᡫ ᡥᡪᡳᢚᡳ ᢘᡭᡬᢞᡭᢊᢔᡪᡧ ᡴᡭᢑᡪᡪᡭᢝ ᢘᡪᢋᡭᡧ ᡳ ᡳᡪᢝᡨ ᡬᡫ ᢔᡬᢞᢙᡪᢙᡪᡬᡨ ᢋᡭᡬᢊᡪᢞᡭᢋᡭᡧ ᡥᡭᡬᢊᡬᡭᡧ ᡭᡭᡧ ᡥᡭᢑᡪᡧ ᡳᡪᡬᡬᢙᡪᢇ ᠃ ᢘᡪᢎᡬᢝ ᡭᡧ ᡥᡭᢞᡭᢑᢙᡭᡱᡱᡪᡧ ᢊᡬᢋᡭ ᡴᡭᢑᡱᡱᡪᡧ ᡬᢟᡪᡬᡨ ᡳᡪᢜᡪᢝ ᡳᡪᡪᡪᢔᡫ ᡬᡬᡧ ᡛ ᡸᡭᡳᡬᡦ ᡬᡬᡧ ᡥᡭᡬᢛᡪᢋᡭᢑᡭᢑᢊᡪᡨ ᡜ ᡴᡭᢑᢛᡳ ᢘᡪᡱᡱᡭᢔᡭᡰᡨ ᠃ ᡥᡭᡪᡪᡭᢝ ᡬᡪᡫ ᡬᡬᡧ ᡥᡭᢞᡭᢑᢙᡭᡱᡱᡪᡧ ᡥᢚᡧ ᡥᢊᡬᢑᡪᢊᡪᡭᡧ ᡸᡭᢑᡧ ᡬᡬᡧ ᢋᡭᡬᢜᡭᢑᢙᡧ ᢘᡪᡱᡱᡭᢔᡪᡪᡳ ᡴᡭᢑᢙᡪᢐᡨ ᢌᡪᢑᢛᡬᡪᡪᡳ ᡥᡭᢞᡭᡧ ᡳ ᡥᡪᡪᢊᢊᡬᡦ ᡬᡬᡧ ᢊᡪᡭᢊᡪᡭᡧ ᡭᡭᡧ ᢌᡪᡪᢊᢑᡪᡫ ᡴᡭᢑᢙᡪᢇ ᡯᡫ ᢊᡪᢚᡪᢋᡭᡳ ᢜᡭᢍ ᠃ ᢘᡪᡱᡱᡪᡱᡱᡭᢑᡭᡪᡪᢔᡪᡧ ᢔᡭᢞᡭᡪᡪᢚᡫ ᡯᡫ ᢘᡪᢎᢙᡪᢊᢑᡪᢐ ᢔᡬᡱᡪᡭᡪᢊᡪᢋᡭ ᡯᡫ ᡬᢟᡪᡬᡨ ᡳᡪᢜᡪᢝ ᡳᡪᡪᡪᢔᡫ ᡬᡬᡧ ᡸᡪᢎᡭᢇ ᢜᡪᢊᡪᡨ ᡥᡭᢎᡭᡪᡪᢔᡬᢐ ᠃ ᢘᡪᢎᢙᡪᢊᢑᡪᢐ ᢔᡬᡱᡪᡭᡪᢊᡪᢊᢔᡪᡧ ᢊᡪᡭᢊᡪᡭᡧ ᡭᡭᡧ ᡳᡪᡪᡪᢔᡫ ᡳᡪᡧ ᢘᡪᡱᡱᡪᡱᡱᡪᡭᡧ ᡥᡭᡬᢙᡪᢔᡫ ᡬᡬᡧ ᡬᡪᡫ ᢙᡳ ᢏᡪᡧ ᢦᡭᡬᡧ ᢌᡭᡪᡪᡭᢑᡪᢇ ᢙᡳ ᡶᡬᢛᡬᡳ ᢔᡬᢎᡪᢋᡭ ᡯᡫ ᡸᡪᢞᡬᢜᡪᢙᡳ ᢔᡭᢞᡱᡱᡪᡱᡱᡭᢑᡫ ᡬᡬᡧ ᢘᡪᡭᢊᡪᡧ ᢘᡪᢊᡪᢞᡪᢊᡬᡦ ᡸᡪᢎᡭᢇ ᡭᡧ ᡥᡭᡬᡪᢊᢊᡪᡨ ᢊᡪᢞᡪᢐ ᢘᡪᡫ ᢊᡪᢞᡪᡬᡨ ᡥᡪᢛᡭᡱᡱᡳ ᠃


ᡥᡭᢞᡪᡪᢙᡭᢜᡪᢉᡨ ᡳᡪᡪᡪᢔᡫ ᢜᡪᢊᡪᡨ ᡥᡭᡬᢛᡪᢔᢋᡭᢑᡪᡪᡬᡨ ᢘᡭᡬᢞᡭᢊᢔᡪᡧ ᢚᡳ ᡴᡭᢞᡭᡪᡪᢛᡬᡱᡱᡭᡧ ᡬᡪᡪᡬᡨ ᢘᡪᡫ ᠃ ᢔᡭᡪᡪᢙᡭᡱᡱᡪᢝ ᢔᡪᡬᡬᡪᡪᡪᡧ ᡳᡪᡫᡨ ᡯᡫ ᡯᡪᢎᡭᢝ ᡭᡧ ᡯᡪᢎᡪᢉᡨ ᢔᡬᢇ ᢈᡪᡪᢊᡱᡱᡪᡪᡬᡨ ᠂ ᢔᡭᡱᡭᢔᡪᡪᡳ ᢙᡳ ᡥᡳᢑᡬᡪᢊᢊᡪᡧ ᡥᡭᢞᢙᡭᢓ ᡥᡪᢜᡪᢑᡱᡱᡳ ᡳᡪᢝ ᢘᡪᡱᡱᡭᡳ ᡥᢞᢋᡭᢋᡭ ᢙᡳ ᢔᡪᡭᡪᢊᡬᢐ ᢙᡳ ᡥᡳᢊᡪᢞᡪᢊᢔᡪᡧ ᡥᡭᢑᡪᡧ ᡥᡭᢚᡬᢝ ᡭᡭᡧ ᢔᡪᢑᢊᡬᡧ ᢙᡳ ᢊᡪᡬᡬᢔᡭᢊᢔᡪᡧ ᢔᡬᡬᡨ ᡥᡭᡬᢊᡪᡫ ᡴᡭᢑᢛᡳ ᡥᡭᢞᢊᡬᡰᡨ ᠃ ᡴᡭᡬᢛᡬᡬᡨ ᢘᡳ ᢚᡳ ᢜᡪᢊᡪᡨ ᡥᡳᢑᡬᡪᢊᢊᡪᡧ ᠂ ᢘᡭᢊᡬᢑᡪᢇ ᢔᡪᡬᡬᡪᡪᡪᡧ ᢌᡭᡬᢞᡳ ᡳᡪᡧ ᡥᡳᢙᡪᡫ ᢔᡪᡬᡬᡪᡪᡪᡧ ᢋᡭᡬᢙᡪᢑᢊᡪᢋᡭ ᢙᡳ ᢘᡭᡱᡱᡭᡱᡳ ᡳᡪᢝ ᢊᡪᡬᡬᢋᡭ ᢔᡪᢞᡧ ᡥᡬᢚᡬᢊᢔᡪᡧ ᢜᡭᢎᢔᡬᢇ ᡴᡭᢑᢙᡪᢇ ᠃ ᡥᡭᢞᡪᡪᢙᡭᢜᡪᢉᡨ ᡳᡪᡪᡪᢔᡫ ᡥᡭᢙᡳ ᡸᡪᢞᡬᢜᡪᢙᡳ ᢔᡭᢞᡱᡱᡪᡱᡱᡭᢑᡫ ᡬᡬᡧ ᢗᡭᡬᢛᡫ ᡬᡬᡧ ᡳᡪᡬᡬᢞᡬᡧ ᢙᡳ ᢊᡬᢑᡪᢞᡪᢛᡳ ᠂ ᢌᡪᢑᢛᡬᡪᡪᡳ ᡥᡭᢞᡭᡧ ᡳ ᡥᡪᡪᢊᢊᡬᡦ ᡬᡬᡧ ᡥᡭᡬᡪᢊᢊᡪᡨ ᢊᡪᢞᡪᢐ ᢘᡪᡫ ᢘᡪᡭᢊᡪᡨ ᡬᡫ ᡥᡭᡬᡳᡪᢝ ᡭᡧ ᡸᡭᡭᢓ ᢈᡪᢝ ᡬᡬᡪᢝ ᡬᡬᡪᡧ ᡥᡭᡬᢞᢋᡭᢑᢛᡬᢑᡪᡧ ᡳᡬᢚᡬᢛᡳ ᡳᡪᡬᡬᡪᡪᡳ ᡯᡫ ᡳᡪᡪᡪᡪᢞᡪᡱᡱᡭᢔᡬᢙᡪᡫ ᡳᡪᡬᡬᡰᡨ ᠃


ᡥᡭᢞᢚᡬᢎᡪᡪᡪᡧ ᡬᢟᡪᡬᡨ ᡳᡪᡪᡪᢔᡫ ᡯᡭᢙᡭᢇ ᡥᢚᡧ ᡳᡪᡧ ᡴᡭᢚᡪᡪᡪᡳ ᡬᡪᢍ ᢙᡳ ᡳᡪᡧ ᢌᡪᡧ ᡳ ᡥᡪᡪᢊᢊᡬᡦ ᡬᡬᡧ ᡥᡭᡭᡪᡪᡪᡭᡪᡳᡪᡱᡱᡪᢙᡭᢝ ᢘᡪᡫ ᡸᡪᢎᢙᡳ ᡯᡪᢎᡪᡬᡫ ᡥᢞᢊᡬᢛᡳ ᡥᡭᡬᢛᡪᢊᢔᡪᡧ ᡳᡬᢑᡧ ᠃ ᡥᡪᢞᡬᢊᡬᡧ ᢔᡬᢞᡪᢊᡪᡰᡨ ᡴᡭᡬᢎᡴᡭᢞᡬᢋᡭ ᡯᡫ ᡴᡭᡬᢝ ᡥᡪᢑᡫ ᡥᢞᢙᡧ ᡬᡬᡧ ᡸᡪᢞᡬᢜᡪᢙᡳ ᢔᡭᢞᡱᡱᡪᡱᡱᡭᢑᡫ ᡬᡬᡧ ᡬᡭᡱᡱᡭᢇ ᡳᡪᡬᡬᢛᡳ ᠂ ᢈᡭᢚᡬᡧ ᡥᡭᡧ ᡳ ᢔᡪᢑᢊᡬᡧ ᡴᡭᢞᡭᡱᡱᡪᡧ ᢔᡪᡭᡪᢊᡬᢐ ᡭᡧ ᡬᡪᡫ ᡬᡫ ᢋᡭᡬᢙᡪᢑᢊᡪᢛᡳ ᢚᡬᢙᡪᢉᡨ ᡥᡭᡬᢊᡪᡫ ᠃ ᡥᡭᡬᢚᡭᢊᡪᢙᡭᢞᢊᡪᡧ ᢜᡭᢎᢔᡬᢇ ᡥᡪᢑᡬᡳᡪᡨ ᡴᡭᡬᢋᡭᡧ ᡯᡬᢙᡭᡧ ᡥᢎᡭᡰᡨ ᡸᡪᢞᡪᡪᡪᢙᡪᡪᡪᢔᡪᡧ ᢜᡭᢍ ᠃

ᡸᡭᢜᡪᢝ ᡳᡪᡪᡪᢔᡫ ᢙᡳ ᡳᡪᡧ ᡥᡪᢛᡧ ᡬᡬᢞᡱᡱᡪᢐ ᢋᡭᡬᢔᡪᡫᡨ ᡒ



